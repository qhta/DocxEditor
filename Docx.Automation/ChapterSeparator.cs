namespace Docx.Automation;

/// <summary>
/// Specifies the separator between chapter number and page number.
/// </summary>
public enum ChapterSeparator
{
  /// <summary>
  /// Hyphen Chapter Separator.
  /// </summary>
  Hyphen,
  /// <summary>
  /// Period Chapter Separator.
  /// </summary>
  Period,
  /// <summary>
  /// Colon Chapter Separator.
  /// </summary>
  Colon,
  /// <summary>
  /// Em Dash Chapter Separator.
  /// </summary>
  EmDash,
  /// <summary>
  /// En Dash Chapter Separator.
  /// </summary>
  EnDash,  
}