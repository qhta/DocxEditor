namespace Docx.Automation;
/// <summary>
/// Specifies the direction in which to move or collapse a range or selection.
/// </summary>
public enum MoveDirection
{
  /// <summary>
  /// Move forward or collapse the range to the ending point.
  /// </summary>
  Forward = 0,
  /// <summary>
  /// Move backward or collapse the range to the starting point.
  /// </summary>
  Backward = 1,
}
